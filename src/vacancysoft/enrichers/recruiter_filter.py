# Keyword fallback — catches agencies not yet in the list above
RECRUITER_KEYWORDS: tuple[str, ...] = (
    "recruitment", "recruiting", "recruiter", "staffing",
    "headhunt", "head hunt", "executive search",
    "talent acqui", "talent solution", "talent partner",
    "personnel agency", "employment agency",
    "search firm", "search partner",
    "placement", "temp agency",
)


def is_recruiter(company: str | None) -> bool:
    """Return True if the company looks like a recruitment agency."""
    if not company:
        return False
    norm = company.strip().lower()
    if norm in EXCLUDED_RECRUITERS:
        return True
    return any(kw in norm for kw in RECRUITER_KEYWORDS)
